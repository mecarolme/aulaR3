#' Function somar
#'
#' This function is used to add two values.
#'
#' @param a first number.
#' @param b another number.
#'
#' @examples
#' sum(100, 50)
#' Console response: 150
#'
#' @export
somar <- function(a,b) {
  a + b
}

#' Function subtract
#'
#' This function is used to subtract two values.
#'
#' @param a first number.
#' @param b another number.
#'
#' @examples
#' subtract(100, 50)
#' Console response: 50
#'
#' @export
subtract <- function(a,b) {
  a - b
}

